                    try:
                        outputs = trained_model(X_batch)
                        batch_probs = torch.exp(outputs)[:, 1].cpu().numpy()
                        all_probs.extend(batch_probs)
                    except Exception as e:
                        print(f"    Eval batch {i//eval_batch_size} failed: {e}")
