def get_quantum_input_from_embedding(embeddings_pca, n_qubits):
    X = embeddings_pca.copy()
    minv = X.min(axis=0)
    maxv = X.max(axis=0)
    rng = maxv - minv
    rng[rng == 0] = 1.0
    Xn = (X - minv) / rng
    Xn = Xn * np.pi
    if Xn.shape[1] >= n_qubits:
        return Xn[:, :n_qubits]
    else:
        pad = np.zeros((Xn.shape[0], n_qubits - Xn.shape[1]))
        return np.concatenate([Xn, pad], axis=1)

